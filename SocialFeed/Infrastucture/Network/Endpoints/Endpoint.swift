import Alamofire

/// Протокол, описывающий API-эндпоинт.
///
/// Используется для конфигурации сетевых запросов, включая путь, метод,
/// параметры, заголовки и мок-ответ (для тестирования).
protocol Endpoint {
    
    /// Путь запроса, начиная с `/`.
    ///
    /// Пример: `/users/profile`
    var path: String { get }
    
    /// HTTP-метод запроса (GET, POST, и т.д.).
    var method: HTTPMethod { get }
    
    /// Параметры запроса (query или body).
    ///
    /// Может быть `nil`, если параметры отсутствуют.
    var parameters: Parameters? { get }
    
    /// Заголовки запроса.
    ///
    /// Может быть `nil`, если заголовки не требуются.
    var headers: HTTPHeaders? { get }
    
    /// Имя файла с мок-ответом (JSON), используемого в тестовой среде.
    ///
    /// Может быть `nil`, если мок-ответ не используется.
    var mockResponseFileName: String? { get }
}
